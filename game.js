const drawWalls = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 1) {
                createRect(j*block_size, i*block_size, block_size, block_size, "#342DCA");
            }
        }
    }
}

const drawFood = () => {
    for (let i = 0; i < map.length; i++) {
        for (let j = 0; j < map[0].length; j++) {
            if (map[i][j] == 2) {
                createRect(j*block_size+block_size/3, i*block_size+block_size/3, block_size/3, block_size/3, "orange");
            }
            if (j > 0 && map[i][j-1] == 1) {
                createRect(j*block_size, i*block_size+wallOffset, wallSpaceWidth+wallOffset, wallSpaceWidth, "black");
            }
        }
    }
}

